def wald_ci(k, n, z=1.96):
    """
    Wald (normal-approximation) CI for a binomial proportion.
    Returns (p_hat, lo, hi) in [0,1].
    """
    if n == 0:
        return 0.0, 0.0, 0.0
    p = k / n
    se = math.sqrt(p * (1.0 - p) / n) if n > 0 else 0.0
    lo = p - z * se
    hi = p + z * se
    lo = max(0.0, lo)
    hi = min(1.0, hi)
    return p, lo, hi

