DATE_ANY_RE = re.compile(r"^\d{4}[-/]\d{1,2}[-/]\d{1,2}$")
DATE_FIND_RE = re.compile(r"\d{4}[-/]\d{1,2}[-/]\d{1,2}")

def normalize_date(s: str):
    s = str(s).strip().strip('"').strip("'")
    if not DATE_ANY_RE.match(s):
        return None
    sep = "-" if "-" in s else "/"
    y, m, d = s.split(sep)
    try:
        y = int(y); m = int(m); d = int(d)
        if not (1 <= m <= 12 and 1 <= d <= 31):
            return None
        return f"{y:04d}-{m:02d}-{d:02d}"
    except Exception:
        return None

def parse_dates_cell(x):
    if pd.isna(x):
        return []
    s = str(x).strip()
    if s == "" or s.lower() in {"none", "nan"}:
        return []
    nd = normalize_date(s)
    if nd:
        return [nd]
    for s_try in (s, s.replace("“", '"').replace("”", '"').replace("'", '"')):
        try:
            v = ast.literal_eval(s_try)
            if isinstance(v, (list, tuple)):
                out = []
                for it in v:
                    nd2 = normalize_date(it)
                    if nd2:
                        out.append(nd2)
                return out
        except Exception:
            pass
    found = DATE_FIND_RE.findall(s)
    out = []
    for it in found:
        nd3 = normalize_date(it)
        if nd3:
            out.append(nd3)
    return out

def parse_int_or_none(x):
    # key fix: treat NaN as None
    if x is None:
        return None
    if isinstance(x, float) and math.isnan(x):
        return None
    if pd.isna(x):
        return None
    s = str(x).strip()
    if s == "" or s.lower() in {"none", "nan"}:
        return None
    try:
        return int(float(s))
    except Exception:
        return None

